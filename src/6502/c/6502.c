void M6502_ForEachFn(M6502_ForEachFnFn fn, void *context) {
    for (size_t i = 0; g_named_fn_lists[i]; ++i) {
        const NamedFn *named_fn = g_named_fn_lists[i];
        while (named_fn->name) {
            (*fn)(named_fn->name, named_fn->fn, context);
            ++named_fn;
        }
    }
}

