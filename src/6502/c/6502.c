void M6502_NextInstruction(M6502 *s) {
    if (!s->d1x1) {
        s->abus.w = s->pc.w;
        s->read = M6502ReadType_Interrupt;
        s->tfn = s->interrupt_tfn;
    } else {
        s->abus.w = s->pc.w++;
        s->opcode_pc = s->abus;
        s->read = M6502ReadType_Opcode;
        s->tfn = &Cycle0_All;
    }
}

